"""Ejercicio 2: Concatenación de tuplas
Tienes las siguientes dos tuplas: tupla1 = (1, 2, 3) tupla2 = (4, 5, 6)
a) Crea una nueva tupla que sea la concatenación de tupla1 y tupla2.
b) Imprime la nueva tupla."""
# Dadas las tuplas
tupla1 = (1, 2, 3)
tupla2 = (4, 5, 6)

# a) Crear una nueva tupla que sea la concatenación de tupla1 y tupla2
nueva_tupla = tupla1 + tupla2

# b) Imprimir la nueva tupla
print(nueva_tupla)